import turtle

#a

t=turtle.Turtle()
t.penup()
t.goto(-30,50) #centering in the screen
t.pendown()
t.pensize(20)
t.pencolor("black")
t.right(65)
t.forward(100)
t.setpos(-30,50)
t.right(50)
t.forward(100)
t.penup()
t.setpos(-50,-10)
t.right(65)
t.pendown()
t.backward(50)


